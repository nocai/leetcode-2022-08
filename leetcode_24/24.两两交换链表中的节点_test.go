/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func swapPairs(head *ListNode) *ListNode {
	dummy := &ListNode{Next: head}
	curr := dummy

	for curr.Next != nil && curr.Next.Next != nil {
		n1 := curr.Next
		n2 := curr.Next.Next

		curr.Next = n2
		n1.Next = n2.Next
		n2.Next = n1

		curr = n1
	}
	return dummy.Next
}

